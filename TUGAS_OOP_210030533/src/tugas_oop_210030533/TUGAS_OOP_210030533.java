package tugas_oop_210030533;
import java.util.Scanner;

public class TUGAS_OOP_210030533 {

    public static void main(String[] args) {
        BankAccount myAcc = new BankAccount();
        myAcc.accountID = "12345";
        myAcc.namaPemilik = "Budi";
        myAcc.saldo = 0;
        Scanner sc = new Scanner(System.in);

        while (true) {
            System.out.print("1:Deposit, 2:Withdraw, 3:CekSaldo, 4:Exit. Pilih >> ");
            int pilihan = sc.nextInt();

            if (pilihan == 1){
                System.out.print("Masukan nominal deposit >> ");
                int nom = sc.nextInt();
                myAcc.deposit(nom);
            }
            else if (pilihan == 2) {
                System.out.print("Masukan nominal withdraw >> ");
                int nom = sc.nextInt();
                myAcc.withdraw(nom);
            }
            else if (pilihan == 3) {
                myAcc.cekSaldo();
            }
            else  if (pilihan == 4) {
                break;
            }
            System.out.println();
        }
    }
}
